$(document).ready(function () {
    $("#restaurant-search").autocomplete({
        source: function (request, response) {
            $.ajax({
                url: "{{ route('search') }}",
                data: {
                    search: request.term
                },
                dataType: "json",
                success: function (data) {
                    response($.map(data, function (item) {
                        return {
                            label: item.name,
                            value: item.name
                        }
                    }));
                }
            });
        },
        minLength: 2
    });
});

